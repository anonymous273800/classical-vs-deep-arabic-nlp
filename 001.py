def build_cnn_lstm_model():
    """Build a CNN-LSTM model for sequence-based Arabic text classification."""
    sequence_input = Input(shape=(MAX_SEQUENCE_LENGTH,), dtype="int32")

    embedding_layer = Embedding(
        input_dim=MAX_NB_WORDS,
        output_dim=EMBEDDING_DIM,
        input_length=MAX_SEQUENCE_LENGTH,
        trainable=True,
    )

    embedded_sequences = embedding_layer(sequence_input)

    x = Conv1D(filters=64, kernel_size=5, activation="relu")(embedded_sequences)
    x = MaxPooling1D(pool_size=5)(x)
    x = Dropout(0.5)(x)
    x = Conv1D(filters=64, kernel_size=5, activation="relu")(x)
    x = MaxPooling1D(pool_size=5)(x)
    x = Dropout(0.3)(x)
    x = LSTM(32)(x)
    predictions = Dense(N_CLASSES, activation="softmax")(x)

    model = Model(sequence_input, predictions, name="CNN_LSTM")
    model.compile(
        loss="categorical_crossentropy",
        optimizer="adam",
        metrics=["accuracy"],
    )

    return model


