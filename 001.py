def balance_dataset(df: pd.DataFrame, samples_per_class: int, seed: int = SEED) -> pd.DataFrame:
    """
    Balance the dataset by sampling a fixed number of documents per class.
    If a class has fewer documents than requested, all available documents are used.
    """
    balanced_parts = []

    for label, group in df.groupby(LABEL_COLUMN):
        n = min(samples_per_class, len(group))
        balanced_parts.append(group.sample(n=n, random_state=seed))

    balanced_df = pd.concat(balanced_parts, axis=0)
    balanced_df = balanced_df.sample(frac=1.0, random_state=seed).reset_index(drop=True)

    return balanced_df


def normalize_arabic(text: str) -> str:
    """Apply light Arabic text normalization."""
    text = str(text)

    # Remove Arabic diacritics and tatweel
    arabic_diacritics = re.compile(r"[\u0617-\u061A\u064B-\u0652]")
    text = re.sub(arabic_diacritics, "", text)
    text = re.sub(r"ـ", "", text)

    # Normalize common Arabic letter variants
    text = re.sub(r"[إأآا]", "ا", text)
    text = re.sub(r"ى", "ي", text)
    text = re.sub(r"ؤ", "و", text)
    text = re.sub(r"ئ", "ي", text)
    text = re.sub(r"ة", "ه", text)

    # Remove URLs, emails, and extra spaces
    text = re.sub(r"http\S+|www\S+", " ", text)
    text = re.sub(r"\S+@\S+", " ", text)
    text = re.sub(r"\s+", " ", text).strip()

    return text


def preprocess_arabic_text(text: str, remove_stopwords: bool = True, stem: bool = True) -> str:
    """Normalize, optionally remove Arabic stopwords, and optionally stem Arabic words."""
    text = normalize_arabic(text)

    words = text.split()

    if remove_stopwords:
        words = [w for w in words if w not in ARABIC_STOPWORDS]

    if stem:
        words = [STEMMER.stem(w) for w in words]

    return " ".join(words)


def plot_history(history, model_name: str):
    """Plot and save training/validation accuracy and loss curves."""
    history_dict = history.history

    # Accuracy key compatibility
    acc_key = "accuracy" if "accuracy" in history_dict else "acc"
    val_acc_key = "val_accuracy" if "val_accuracy" in history_dict else "val_acc"

    plt.figure()
    plt.plot(history_dict[acc_key], label="Train Accuracy")
    plt.plot(history_dict[val_acc_key], label="Validation Accuracy")
    plt.title(f"{model_name}: Accuracy per Epoch")
    plt.xlabel("Epoch")
    plt.ylabel("Accuracy")
    plt.legend()
    plt.tight_layout()
    plt.savefig(FIGURE_DIR / f"{model_name.lower().replace(' ', '_')}_accuracy.png", dpi=300)
    plt.show()

    plt.figure()
    plt.plot(history_dict["loss"], label="Train Loss")
    plt.plot(history_dict["val_loss"], label="Validation Loss")
    plt.title(f"{model_name}: Loss per Epoch")
    plt.xlabel("Epoch")
    plt.ylabel("Loss")
    plt.legend()
    plt.tight_layout()
    plt.savefig(FIGURE_DIR / f"{model_name.lower().replace(' ', '_')}_loss.png", dpi=300)
    plt.show()


def plot_confusion_matrix(cm, class_names, model_name: str):
    """Plot and save confusion matrix without requiring seaborn."""
    plt.figure(figsize=(8, 6))
    plt.imshow(cm, interpolation="nearest")
    plt.title(f"{model_name}: Confusion Matrix")
    plt.colorbar()
    tick_marks = np.arange(len(class_names))
    plt.xticks(tick_marks, class_names, rotation=45, ha="right")
    plt.yticks(tick_marks, class_names)

    threshold = cm.max() / 2.0
    for i in range(cm.shape[0]):
        for j in range(cm.shape[1]):
            plt.text(
                j,
                i,
                format(cm[i, j], "d"),
                ha="center",
                va="center",
                color="white" if cm[i, j] > threshold else "black",
            )

    plt.ylabel("True Label")
    plt.xlabel("Predicted Label")
    plt.tight_layout()
    plt.savefig(FIGURE_DIR / f"{model_name.lower().replace(' ', '_')}_confusion_matrix.png", dpi=300)
    plt.show()


def evaluate_predictions(y_true, y_pred, class_names, model_name: str):
    """Compute and print standard classification metrics."""
    accuracy = accuracy_score(y_true, y_pred)
    precision_macro, recall_macro, f1_macro, _ = precision_recall_fscore_support(
        y_true, y_pred, average="macro", zero_division=0
    )
    precision_weighted, recall_weighted, f1_weighted, _ = precision_recall_fscore_support(
        y_true, y_pred, average="weighted", zero_division=0
    )

    print("\n" + "=" * 70)
    print(f"{model_name} Evaluation")
    print("=" * 70)
    print(f"Accuracy:          {accuracy:.4f}")
    print(f"Macro Precision:   {precision_macro:.4f}")
    print(f"Macro Recall:      {recall_macro:.4f}")
    print(f"Macro F1:          {f1_macro:.4f}")
    print(f"Weighted F1:       {f1_weighted:.4f}")
    print("\nClassification Report:")
    print(classification_report(y_true, y_pred, target_names=class_names, zero_division=0))

    cm = confusion_matrix(y_true, y_pred)
    plot_confusion_matrix(cm, class_names, model_name)

    return {
        "Model": model_name,
        "Accuracy": accuracy,
        "Macro Precision": precision_macro,
        "Macro Recall": recall_macro,
        "Macro F1": f1_macro,
        "Weighted Precision": precision_weighted,
        "Weighted Recall": recall_weighted,
        "Weighted F1": f1_weighted,
    }


def evaluate_neural_model(model, x_test, y_test_categorical, y_test_labels, class_names, model_name: str):
    """Evaluate a Keras neural model."""
    y_prob = model.predict(x_test, verbose=0)
    y_pred = np.argmax(y_prob, axis=1)

    result = evaluate_predictions(y_test_labels, y_pred, class_names, model_name)

    try:
        auc = roc_auc_score(y_test_categorical, y_prob, multi_class="ovr")
        result["AUC_OVR"] = auc
        print(f"AUC OVR:           {auc:.4f}")
    except Exception as e:
        result["AUC_OVR"] = np.nan
        print(f"AUC could not be computed: {e}")

    return result


