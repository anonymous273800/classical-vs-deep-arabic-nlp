def load_dataset(data_path: str) -> pd.DataFrame:
    """Load Arabic text classification dataset."""
    data_path = find_dataset_file(data_path)
    print(f"Loading dataset from: {data_path}")

    df = pd.read_csv(data_path)

    if TEXT_COLUMN not in df.columns or LABEL_COLUMN not in df.columns:
        raise ValueError(
            f"Expected columns '{TEXT_COLUMN}' and '{LABEL_COLUMN}'. "
            f"Found columns: {list(df.columns)}"
        )

    df = df[[TEXT_COLUMN, LABEL_COLUMN]].dropna().reset_index(drop=True)
    df[TEXT_COLUMN] = df[TEXT_COLUMN].astype(str)
    df[LABEL_COLUMN] = df[LABEL_COLUMN].astype(str)

    # Remove accidental header-like rows if they exist in concatenated CSV files.
    df = df[df[TEXT_COLUMN].str.lower() != TEXT_COLUMN.lower()].reset_index(drop=True)
    df = df[df[LABEL_COLUMN].str.lower() != LABEL_COLUMN.lower()].reset_index(drop=True)

    return df


