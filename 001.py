def predict_text_examples(text_examples, model_type="svm"):
    """Predict labels for raw Arabic text examples using a selected model."""
    if isinstance(text_examples, str):
        text_examples = [text_examples]

    if model_type == "svm":
        processed = [preprocess_arabic_text(t, remove_stopwords=True, stem=True) for t in text_examples]
        preds = svm_pipeline.predict(processed)
        return preds

    if model_type == "dnn":
        normalized = [normalize_arabic(t) for t in text_examples]
        seq = tokenizer.texts_to_sequences(normalized)
        padded = pad_sequences(seq, maxlen=MAX_SEQUENCE_LENGTH)
        probs = dnn_model.predict(padded, verbose=0)
        preds = label_encoder.inverse_transform(np.argmax(probs, axis=1))
        return preds

    if model_type == "cnn_lstm":
        normalized = [normalize_arabic(t) for t in text_examples]
        seq = tokenizer.texts_to_sequences(normalized)
        padded = pad_sequences(seq, maxlen=MAX_SEQUENCE_LENGTH)
        probs = cnn_lstm_model.predict(padded, verbose=0)
        preds = label_encoder.inverse_transform(np.argmax(probs, axis=1))
        return preds

    raise ValueError("model_type must be one of: 'svm', 'dnn', or 'cnn_lstm'.")


